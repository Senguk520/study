#include <bits/stdc++.h>
using namespace std;

/*
题意转化：

给每个顶点分配一个 1 ~ k 的“顶点对应的数”。
如果选择边 (u, v)，这条边就会占用一个无序数对：
    (min(num[u], num[v]), max(num[u], num[v]))

每个数对只能用一次，所以：
    如果两条边得到同一个数对，最多只能保留其中一条。

目标：让最多的图中边可以被这些数对替代。
k是数对、n是顶点数、m是边
------------------------------------------------------------
关键观察 1：如果 n <= k，答案一定是 m

因为顶点数量不超过数字数量，可以给每个顶点分配不同的数。

例子：
    k = 5, n = 4
    顶点 1,2,3,4 可以分别分配数字 1,2,3,4

    边 (1,2) -> 数对 (1,2)
    边 (2,3) -> 数对 (2,3)
    边 (3,4) -> 数对 (3,4)
    边 (4,1) -> 数对 (1,4)

由于图是简单无向图，同一条边不会重复；
又因为每个顶点数字不同，所以不同边不会得到同一个数对。
因此所有 m 条边都能替换。

------------------------------------------------------------
关键观察 2：只有 n = k + 1 时需要考虑冲突

有 k 个数字，却有 k + 1 个顶点。
根据抽屉原理，至少有两个顶点必须使用同一个数字。

最优情况下，可以认为：
    只有两个顶点共用一个数字，其他顶点都用不同数字。

为什么？
    如果某些数字没用，而某个数字被很多顶点共用，
    把其中一个顶点改成没用过的数字，不会制造新的冲突，
    只可能减少原来的冲突。

所以 n = k + 1 时，问题变成：
    选择哪两个顶点共用同一个数字，损失最少？

------------------------------------------------------------
如果顶点 a 和 b 共用同一个数字，什么时候会损失边？

设 a 和 b 都分配数字 1，另一个顶点 c 分配数字 2。

如果边 (a,c) 和 (b,c) 都存在：
    (a,c) -> 数对 (1,2)
    (b,c) -> 数对 (1,2)

这两个数对相同，但一个数对只能用一次，
所以这两条边最多只能保留一条，损失 1 条边。

--但此时需要a=b

这正好对应：
    c 是 a 和 b 的共同邻居。

因此：
    如果让 a 和 b 共用数字，损失 = a 和 b 的共同邻居数量。

答案就是：
    m - min(任意两个顶点的共同邻居数量)

------------------------------------------------------------
样例 2：
    k = 6, n = 7
    图是 7 个点的完全图，一共有 21 条边。

任意两个顶点都有另外 5 个共同邻居。
所以无论哪两个点共用数字，都会损失 5 条边。

答案：
    21 - 5 = 16
*/

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int k, n, m;
    cin >> k >> n >> m;

    vector<vector<int>> g(n + 1, vector<int>(n + 1, 0));

    for (int i = 0; i < m; i++) {
        int u, v;
        cin >> u >> v;
        g[u][v] = g[v][u] = 1;
    }

    /*
    情况 1：n <= k

    例子：
        k = 5, n = 4
        4 个顶点可以分别使用 1,2,3,4 这 4 个不同数字。

    所有边对应的数对都不会重复，所以答案就是边数 m。
    */
    if (n <= k) {
        cout << m;
        return 0;
    }

    /*
    情况 2：n = k + 1

    枚举哪两个顶点 a, b 共用一个数字。
    对每一对 a, b，统计它们的共同邻居数量 common。

    例子：
        如果 a 连接 c，b 也连接 c，
        那么边 (a,c) 和 (b,c) 会占用同一个数对，
        必须损失其中一条。

    所以我们要找最小 common。
    */
    int minCommon = n; // 共同邻居数量最多也不会超过 n - 2

    for (int a = 1; a <= n; a++) {
        for (int b = a + 1; b <= n; b++) {
            int common = 0;

            for (int c = 1; c <= n; c++) {
                if (c == a || c == b) continue;

                if (g[a][c] && g[b][c]) {
                    common++;
                }
            }

            minCommon = min(minCommon, common);
        }
    }

    cout << m - minCommon;
    return 0;
}
