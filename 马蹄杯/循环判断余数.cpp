
//余数版本
#include <bits/stdc++.h>
using namespace std;

int main() {
    int a, m;
    cin >> a >> m;

    int r  = a%m;
    vector<int>dui(m,0);
    while(r!=0&& !dui[r]){//前面的条件是判断余数是否为0，后面的条件是判断是否进入到余数循环中
        dui[r]=1;
        r = (2*r)%m;
    }

    cout <<(r == 0 ?"Yes":"No");


}



